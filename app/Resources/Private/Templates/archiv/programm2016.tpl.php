    <div style="display: block;" aria-hidden="false">
        <p>Viele Menschen haben heutzutage das Gefühl, in den unsichersten
            Zeiten überhaupt zu leben. Dabei ist das Leben trotz gegenwärtiger
            Krisen gerade in Europa so sicher wie nie zuvor. Gleichzeitig scheint
            immer weniger akzeptiert zu werden, dass die Welt trotz zunehmender
            Sicherheit immer eine riskante, ungewisse bleiben wird. Wir brauchen
            daher eine Gesellschaft, in der Bürgerinnen und Bürger informiert mit
            Risiken umgehen und leben. Doch ein informierter Umgang mit Risiko
            bleibt für viele eine Herausforderung. Anhand von Beispielen aus dem
            Bereich Gesundheit werde ich illustrieren, (i) dass ein mangelhafter
            Umgang mit Risiko ein kollektives Problem ist und keinesfalls nur Laien
