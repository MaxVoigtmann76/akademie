<h2 id="Wehner">Oberflächenreaktionen: Transformation auf atomarer Ebene
    (Vortrag)</h2>
<p>
    Atome an Oberflächen von Festkörpern sind etwas ganz besonderes, da ihnen ein oder mehrere Nachbarn fehlen. Dieses
    Defizit führt zu einer erhöhten Reaktivität. Atome und Moleküle aus der Gasphase docken bevorzugt an diesen
    Stellen an. Dies ist Grundvoraussetzung für die heterogene Katalyse an Festkörperoberflächen, wie wir sie
    beispielsweise im Autokatalysator verwenden. In diesem Beispiel transformiert der Dreiwegekatalysator, genauer
    gesagt kleine Metallpartikel auf einem Keramikkörper, die giftigen Überbleibsel der Verbrennung (Kohlenmonoxid,
    Stickoxide) in ungiftige (Kohlendioxid, Stickstoff).
    Auf Oberflächen können die adsorbierten Moleküle zerfallen und andere neu gebildet werden. Diffusion, die zufällige
    Bewegung von Atomen und Moleküle auf einer Oberfläche, bringt die Reaktionspartner zusammen und entsteht mit einer
    bestimmten Wahrscheinlichkeit das Produkt.
    Der Vortrag behandelt die Physik solcher Oberflächenreaktionen, die Transformation auf atomarer Ebene darstellen. Er
    thematisiert deren Bedeutung, die zugrunde liegende Reaktionsmechanismen und gibt Einblicke in einige aktuelle
    Untersuchungen und Fragestellungen auf diesem Gebiet.

</p>
