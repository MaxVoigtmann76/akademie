<h2 id="Christ">Transformation von Risiken (Vortrag)</h2>
<p>
    Der Grund für die Konstruktion vieler komplexer Finanzprodukte ist die Transformation von Risiken. Die
    transformierten Risiken sollen passgenau abgestimmt sein auf den Risikoappetit des Interessenten, bzw. des
    Investors. Grundsätzlich ist die Risikotransformation eine der zentralen und wichtigen Aufgaben des Finanzsektors.
    Trotzdem kommt z.B. Warren Buffet zu der Schlussfolgerung, dass Derivate (als ein Beispiel für komplexe
    Finanzprodukte)Massenvernichtungswaffen seien. Auch für die jüngste (Finanz-)Krise werden sie als Schuldige
    dargestellt. Der Vortrag wird auf die Funktionsweise von prominenten komplexen Finanzprodukten eingehen. Es werden
    quantitative und strukturelle Gründe für Fehlbewertungen betrachtet, und ein Ausblick in die Zukunft dieser Art von
    Risikotransformation gewagt.
</p>
