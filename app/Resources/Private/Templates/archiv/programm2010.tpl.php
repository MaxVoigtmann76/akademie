<h2 id="Wolf">Stress und Gedächtnis (Vortrag)</h2>
<p>Stress beeinflusst unser Gedächtnis. Hormone aus der Nebenniere (u.a. Cortisol) spielen hierbei eine zentrale Rolle.
    Stress kann sowohl verbessernde als auch verschlechternde Wirkungen auf das Langzeitgedächtnis ausüben. So vergessen
    wir z.B. eine Verabredung, weil wir bei der Arbeit im Stress waren. Anderseits erinnern wir uns an besonders
    emotional erregende Ereignisse oft ein ganzes Leben lang. Diese entgegengesetzten Befunde spiegeln die
    unterschiedlichen Wirkungen von Stress auf unterschiedliche Gedächtnisphasen wieder (Konsolidierung versus Abruf)
    wieder. Ein besseres Verständnis der Auswirkungen von Stress auf die Gedächtnisleistung ist für verschiedene
    psychologische Anwendungsbereiche (klinische Psychologie, pädagogische Psychologie, Wirtschaftspsychologie) von
    Relevanz.
</p>
