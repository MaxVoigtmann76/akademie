<h2 id="Messner">"Globalisierung 1.0 bis 3.0" - Die große Transformation der Weltwirtschaft (Vortrag)</h2>

<p>
    Die nun schon zwanzig Jahre alte Globalisierungsdebatte, geht in eine
    neue Runde. Die "Globalisierungsdiskussion 1.0" (Entgrenzung der
    Ökonomie) ging davon aus, dass sich das Wirtschaftsmodell der
    westlichen Marktwirtschaften nach der Implosion des Sozialismus
    weltweit dynamisch ausdehnen würde: "globalization as westernization".
    Die aktuelle Weltwirtschaftskrise dokumentiert, dass eine
    wohlstandfördernde Globalisierung nur möglich ist, wenn internationale
    Regelwerke und governance-Strukturen etabliert werden, die für
    Stabilität sowie fairen Interessausgleich in der globalen Ökonomie
    sorgen. Es geht um die wieder - Einbettung der Weltwirtschaft in (welt)
    gesellschaftliche Normen- und Regelsysteme.
    Die "Globalisierungsdiskussion 2.0" (tektonische globale
    Machtverschiebungen) setzte zu Beginn der letzten Dekade ein, als
    immer deutlicher wurde, dass die Globalisierung keineswegs den
    Siegeszug der westlichen Industrieländer beschleunigt. Vielmehr wird
    Asien zu einem neuen Gravitationszentrum der Weltwirtschaft, mit China
    und Indien als Lokomotiven. Die 200jährige Dominanz der "alten
    Industrieländer" steht zur Disposition. Die
    "Globalisierungsdiskussion 2.0" befasst also sich mit den Chancen und
    Risiken globalen Regierens in einer multipolaren Machtkonstellation.
    Der "Globalisierungsdiskurs 3.0" (globale Entwicklung und Erdsystem)
    entsteht aus der Debatte um "Klimawandel und Entwicklung". Die
    Klimaforschung zeigt, dass die Menschheit die vier wichtigsten
    Grundlagen jedweder menschlichen Zivilisation destabilisieren könnte,
    wenn die Weltwirtschaft auf ihrem "high carbon &mdash; Pfad" verbleibt: (1)
    Wasser, (2) landwirtschaftliche Flächen (Nahrung) und (3) die
    Atmosphäre könnten im Verlauf der nächsten Dekaden zu knappen Gütern
    werden. Zudem muss aufgrund der Klimakrise (4) die weltweite
    Energieproduktion in den kommenden Dekaden vollständig umgebaut
    werden, von einem fossilen zu einem auf erneuerbaren Energieträgern
    basierten System. "Globalisierung 3.0" bedeutet demnach, dass die
    Weltgesellschaft lernen muss, Weltwirtschaft und -politik innerhalb
    der Grenzen des Erdsystems zu gestalten.<br>
    In dem Vortrag werden diese drei Wellen globalen Wandels diskutiert,
    weltwirtschaftliche und -politische Szenarien vorgestellt und
    skizziert, wie eine Transformation in Richtung einer nachhaltigen
    Entwicklung der Weltgesellschaft aussehen könnte.
</p>
