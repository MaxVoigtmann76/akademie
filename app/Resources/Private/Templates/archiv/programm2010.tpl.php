<h2 id="Fackler2">Transformation der Arbeitswelt vom militärischen Top-Down zum Mitarbeiterzufriedenheitsmanagement. Ein
    Praxisbericht (Vortrag)</h2>
<p>In der Industrialisierung mussten Menschen anfangs wie Maschinen funktionieren: Immer derselbe Handgriff am
    Fließband, etc. Was zu tun war, war schematisch vorgeschrieben, top-down wie beim Militär. Das war der damaligen
    Arbeitsstruktur angemessen, aber eine stetige wachsende Zahl von Menschen hat heute ganz andere Aufgaben, z.B.
    flexibles Troubleshooting oder produktives Verarbeiten von Wissen in vernetzten Teams. Das bringt das
    Firmenmanagement in einen Zwiespalt. Einerseits soll und will es dem Unternehmen die Richtung vorgeben, andererseits
    sind die Tätigkeiten nicht mehr so, dass man sie auf einfache Weise vorschreiben und kontrollieren kann, z.B. dann,
    wenn man auf hohe Flexibilität, schnelles autonomes Handeln einzelner Mitarbeiter oder gar auf Kreativität
    angewiesen ist. Eine zentrale und wachsende Rolle spielen heutzutage Vertrauen und Loyalität, die stark von der
    Zufriedenheit der Angestellten und zahllosen menschlichen und kulturellen Faktoren abhängen. In diesem lang
    dauernden Umbruch des Wirtschaftslebens kann man oft ein Nebeneinander mehrerer Generationen von
    Unternehmensphilosophien beobachten und erlebt ein Hin und Her zwischen Aufbruchsstimmung und konservativen
    Reflexen. Und man könnte (und sollte) ein paar grundsätzliche Fragen zur Unternehmensführung in der
    Wissensgesellschaft stellen.
</p>
