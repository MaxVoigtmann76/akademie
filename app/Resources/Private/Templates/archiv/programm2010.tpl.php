    haben, als Katastrophen bezeichnet werden, hängt nicht nur von tatsächlichen, statistisch erfassbaren
    Unfallfaktoren ab, sondern offensichtlich auch mit dem Katastrophenbewusstsein des jeweiligen Betrachters zusammen,
