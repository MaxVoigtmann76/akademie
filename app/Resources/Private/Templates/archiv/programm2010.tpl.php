<h2 id="Ziegert">Programme transformieren Daten, Programmiersprachen die Softwareentwicklung
    (Vortrag)</h2>

<p>
    In der Softwareentwicklung hat vieles mit Transformation zu tun: Programme transformieren Daten; Programmierer
    transformieren Programmcode entsprechend einer Spezifikation, Informatiker die Spezifikationen, Compiler den
    Programmcode zu Programmen. Bei jeder Transformation können Fehler passieren. Die meisten sind Bugs von
    Programmierern oder Designfehler der Informatiker. Bugs sind zwar ungewollt aber irgendwie sozial akzeptiert. Würde
    man dennoch Schuldzuweisungen machen, würden diese dort enden, wo die Fehler hätten vermieden werden können.
    Paradoxerweise enden sie selten bei der Wahl der Programmiersprache.
    Auf interessante Weise transformiert die Wahl der Sprache nicht einfach nur die Kompetenzen der Programmierer,
    sondern insbesondere Bugs und Designfehler zu Syntaxfehlern, welche der Compiler nicht akzeptiert. In diesem Vortrag
    werden die diesbezüglich wichtigsten Unterschiede von Programmiersprachen aufgezeigt. Dabei wird auch erklärt, was
    "Objektorientierung" und "Funktionale Programmierung" bedeuten.
    Der Vortrag richtet sich nicht nur an erfahrene Programmierer/Informatiker: die benötigten Vorkenntnisse (ähnlich
    wie beim Wikipedia-Artikel "Programmiersprache") werden während des Vortrags in wenigen Sätzen vermittelt.
</p>

