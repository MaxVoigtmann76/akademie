<h2><a id="benedek"></a>Wie lässt sich Kreativität messen? (Vortrag)</h2>

<p>Wie lässt sich Kreativität messen? Eine Antwort darauf hängt natürlich
    wesentlich von der Definition des Konstrukts ab. Ein gängiger Ansatz im
    Rahmen psychometrischer Testverfahren ist der Einsatz von divergenten
    Denkaufgaben. Dabei können sowohl quantitative (Produktivität) als auch
    qualitative (z.B. Originalität) Aspekte des Ideenverhaltens quantifiziert
    werden. Kreative Personen zeichnen sich demnach durch besonders viele und
    ungewöhnliche Ideen aus, was wiederum als Maß für das Potenzial zu
    kreativem Schaffen angenommen wird. Unter der Annahme, dass originelle
    Ideen durch geeignete Kombination entfernter assoziativer Elemente
    entstehen, ergibt sich des weiteren die Möglichkeit interindividuelle
    Unterschiede in der assoziativen Struktur zu messen.</p>

<p><b>Mathias Benedek</b> studierte technische Mathematik und Psychologie an der
    Karl-Franzens-Universität Graz, Österreich. Im Rahmen seiner Diplomarbeit
    entwickelte und evaluierte er ein computergestütztes Training zum
    divergenten Denken. Am Arbeitsbereich für Differentielle Psychologie war an
    Forschungsprojekten zu den neurophysiologischen Korrelaten des kreativen
    Denkens sowie zu kreativitätsförderlichen Interventionen am Arbeitsplatz
    beschäftigt. Er gab Lehrveranstaltungen zur Testkonstruktion - natürlich
    ging es um die Entwicklung von Kreativitätstests. In seiner aktuellen
    Promotion interessiert er sich vor allem für den Zusammenhang von
    Assoziationsleistungen und Kreativität. Seit März 2007 ist er als
    wissenschaftlicher Mitarbeiter an der Christian-Albrechts-Universität zu
    Kiel beschäftigt.</p>

