<h2><a id="dresler"></a>Der Zusammenhang von Kreativität, Schlaf und Traum aus Sicht der Neurowissenschaft (Vortrag)
</h2>
<p>Kekulé und der Benzolring, Mendelejew und das Periodensystem, Loewi und die
    Nervenimpulsübertragung, Howe und die Nähmaschine, Coleridge und Kubla Khan,
    Tartini und die Teufelstrillersonate, McCartney und Yesterday &#8211; die Geschichte
    der Wissenschaft und Kunst ist voll von Berichten und Legenden über kreative
    Leistungen, die während des Schlafens oder Träumens entstanden sind. Über solch
    unsystematische Fallberichte hinaus stellt der Vortrag empirische Forschungen
    über den Zusammenhang von Kreativität, Schlaf und Traum vor und versucht, die
    neurowissenschaftlichen Grundlagen dieser Phänomene
