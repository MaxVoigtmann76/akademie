<h2><a id="brodbeck"></a>Wirtschaft als kreativer Prozess. Wie ein weicher Faktor harte Tatsachen schafft (Vortrag)</h2>
<p>Der wirtschaftliche Wachstumsprozess wird durch Innovationen angetrieben.
    Die Quelle der Innovationen bleibt hierbei allerdings oft im Dunkeln: Die
    menschliche Kreativität. Wissenschaft und Praxis denken vor allem in "harten"
    Faktoren, in Größen, die man messen oder in die Bilanz einstellen kann.
    Tatsächlich erweist sich aber der Versuch, alles planen, kontrollieren und
    jede Minute der Zwangsjacke des Zeitmanagements unterwerfen zu wollen, als
    wichtigstes Kreativitätshemmnis. Kreativität lässt sich nicht "machen". Sie hat
    keine definierten Ursachen und lässt sich deshalb nicht wie ein Herstellungsprozess planen.
    Die neuere Kreativitätsforschung kann zeigen, dass es gerade die "weichen" Faktoren
    sind, die Neuerungen hervorbringen und "harte" Tatsachen schaffen.</p>

<p><b>Dr. Karl-Heinz Brodbeck</b>, Jahrgang 1948, ist Professor für
    Volkswirtschaftslehre und Kreativitätstechniken an der Fachhochschule
    Würzburg. Nach dem Diplom in Elektrotechnik studierte er Philosophie und
    Volkswirtschaftslehre an der Uni München und war dort Akademischer Rat,
    später war er Referent beim Ifo-Institut in München und Geschäftsführer einer
    Beratungsgesellschaft, bevor er 1992 an die FH Würzburg berufen wurde.
    Hauptarbeitsgebiete sind Theorie und Philosophie der Wirtschaft, Ethik und
    Kreativitätsforschung. Brodbeck ist Autor von 16 Büchern und über
    100 Fachartikeln, darunter "Entscheidung zur Kreativität" (1995, 3. Aufl. 2007),
    "Erfolgsfaktor Kreativität" (1996), "Die fragwürdigen Grundlagen der Ökonomie"
    (1998, 3. Aufl. 2007), "Mut zur eigenen Kreativität" (2000).<br>
    Homepage: <a href="http://www.khbrodbeck.homepage.t-online.de">www.khbrodbeck.homepage.t-online.de</a>.
</p>


