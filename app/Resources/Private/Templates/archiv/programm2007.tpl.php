<h2><a id="wiesemann"></a>Von den Vorzügen, erkennbar unsicher an Projekte heranzugehen</h2>

<p>Viele glauben, dass eine aggressiv zur Schau getragene Sicherheit
    Voraussetzung dafür ist, Finanzierung oder Mitmacher zu finden. Ich
    plädiere dafür, sich ein Umfeld zu schaffen, in dem man die eigene
    Unsicherheit ganz offen lebt: Projektgröße so begrenzen, dass die
    vorhandenen Mittel bequem und langfristig dafür reichen, um nicht zeitlich
    unter Druck zu geraten. Und sich selbst zu Beginn eines Weges deutlich als
    "Lernenden" positionieren, weil man nur dann alle Freiheiten für
    Richtungsänderungen und Weiterentwicklungen bekommt. Meine eigenen Projekte
    kenne ich am besten, deshalb möchte ich diese als Beispiele nutzen
    (insbesondere das Unperfekthaus). Ich glaube jedoch, dass sogar Firmen wie
    Google (mit ihren endlosen öffentlichen Beta-Phasen aller Dienste) genau
    dieses Prinzip verfolgen.</p>

<p><b>Reinhard Wiesemann</b> (47) gründete mit 18 die sehr erfolgreiche Firma <a href="http://www.WuT.de/">W&amp;T</a>,
    die Computertechnik entwickelt und produziert, mit 37 das
    erfolglose <a href="http://www.patentwerk.de/">Patentwerk</a>, das Erfindungen verkaufen soll,
    damit andere die Hauptarbeit tun, mit 40 das sehr erfolgreiche <a href="http://www.linuxhotel.de/">Linuxhotel</a>,
    und mit 45 das wirtschaftlich bisher erfolglose (aber
    inhaltlich erfolgreiche) <a href="http://www.unperfekthaus.de/">Unperfekthaus</a>, ein
    ehemaliges Kloster, in dem Kreative aller Fachbereiche alles kostenlos
    bekommen, damit sie risikolos eigene Projekte ausprobieren können. Ich habe
    Spaß daran, neue Dinge aufzubauen, übergebe sie anschließend an andere,
    bleibe aber verbunden (&amp; beteiligt ;-)), so dass ich neue Projekte jeweils
    aus den Erfolgen bisheriger Projekte finanzieren und vor allem auch auf die
    Ressourcen der bisherigen Projekte zurückgreifen kann. Ich hoffe, dass das
    so weitergeht ...</p>

