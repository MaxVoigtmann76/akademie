<h2><a id="mainzer"></a>Der kreative Zufall. Wie das Neue in die Welt kommt. (Vortrag)</h2>
<p>Die Welt, in der wir leben, ist nicht vorbestimmt, sondern zufällig. Wie aber kann aus Zufall und Chaos Ordnung
    entstehen? Viele zufällige Einzelereignisse können zusammen nichtzufällige Eigenschaften haben. So entstehen neue
    Ordnungen in einem Meer von Zufallsfluktuationen. Ohne Zufall entsteht nicht Neues. Nicht immer sind die Ergebnisse
    aber zu unseren Gunsten - von Krankheiten bis zu verrückten Märkten und Menschen mit krimineller Energie. Der Zufall
    lässt sich zwar nicht kontrollieren. Wir können aber seine Systemgesetze verstehen. Mitgeeigneten Nebenbedingungen
    lassen sich dann Voraussetzungen schaffen, unter denen zufällige Ereignisse erwünschte Effekte auslösen. System
    können dazu gebracht werden, Resultate zu erzeugen, die zu unseren Gunsten wirken - in Natur, Technik und Medizin,
    auf Märkten, in Kultur und Gesellschaft. Der Zufall erzeugt dann Sinn für uns. Aus dem blinden wird der kreative
    Zufall. Eine Garantie für das Gelingen gibt es allerdings nicht. Das ist der Preis des Zufalls. Wir leben nicht in
    einer Welt, in der alles von selber gut wird. Nichts wird gut, wenn wir nicht aufpassen. In der Welt der
    Zufallsfluktuationen kommt es darauf an, den günstigen Augenblick zu erkennen. Man muss sein wie Noah: rechtzeitig
    die Zeichen der Zeit erspüren und sein Schiff bauen.
</p>

