<h2 id="Bardocz">Problems with GMOs, health effects and some ethical considerations (Vortrag)</h2>
<p>Although science and scientific progress is very important to understand the world around us, and might help to solve
    some of the problems faced by the Human race today, putting the results of such a technology into practical use too
    early and without proper testing might jeoperdise our health and harm our environment. Recombinant DNA technology is
    one of these fields in science, controlled unfortunately mostly by industrial and financial interests. Based on our
    understanding the products of this irreversible technology was put into practical use too early, without
    understanding its implicatications and without proper testing. All those who try to use scientific arguments to
    point out the shortcomings of GM biotechnology are considered to be enemies of progress or science, or both. Several
    problems of producing and testing GMOs, including nutritional and ethical considerations will be discussed during
    the lecture.</p>
<p><strong>Prof. Habil. Dr. D.Sc. Susan Bardocz</strong> has a BSc and MSc in chemistry, a PhD in Biochemistry and
    Pharmacology, a DSc from the Hungarian Academy of Sciences. She is a professor of Human Nutrition. She published
    over 200 refereed scientific papers, over a 100 book chapters and has written and edited a dozen books. She worked
    in Hungary, Britain and for shorter times in Canada and the USA. Her main reserach interest is the effects of
    biologically active components on cellular and body metabolism. She was the Head of the Food - Gut - Microbial
    Interaction Unit at the Rowett Institute in Aberdeen, Scotland working together with her husband, Arpad Pusztai also
    on the safety of GM plants. She retired from this post in 2000, and became Professor of Human Nutrition at Debrecen
    University in Hungary. She is a scientific advisor of the Hungarian Government on GMO related issues.</p>
