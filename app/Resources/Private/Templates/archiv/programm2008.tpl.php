<h2><a id="lahmer"></a>Number forms - synästhetische Zeitwahrnehmung. Oder: mein Tag ist ein Möbiusband (Vortrag)</h2>
<p>Synästhesie ist eine besondere Form der Wahrnehmung, bei der sonst voneinander getrennte Sinne miteinander vernetzt
    sind. So wird z.B. Musik in Farben und/oder Formen wahrgenommen und ist im Raum verortet. Die synästhetische
    Wahrnehmung kann nicht willentlich beeinflusst werden und geht besonders häufig mit Hochbegabung und erhöhter
