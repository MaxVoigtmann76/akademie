<h2><a id="zehrer"></a>Spannung, Sport und Schiri-Schelte - Zeit und Beschleunigung im modernen Mediensport (Vortrag)
</h2>
<p>Die Zeit kann man sich als Menge der Momente vorstellen, die von Menschen in Bezug zu unterschiedlichen
    Wahrnehmungsinhalten und deren Weiterverarbeitung durch das Denken erlebt werden. Je mehr Wahrnehmungsinhalte pro
    Moment eintreffen, als desto aufregender wird eine Situation erlebt. Das gilt auch für Sportereignisse, bei denen
    die Live- und Fernsehzuschauer von spannenden, immer raffinierter und auch immer temporeicher werdenden Matches
    angezogen werden. Sportler und Trainer können sich dem Beschleunigungsdiktat sportlicher "Höher, besser,
    weiter"-Logik kaum entziehen. Für die menschliche Wahrnehmung jedoch ist irgendwann die Grenze zwischen Spannung und
    Unerkennbarkeit überschritten. Die Zuschauer erhalten dann eine Zeitlupe und die Schiedsrichter - nicht selten
    Schelte. Der Beitrag setzt sich mit den Wechsel- und Nebenwirkungen von Beschleunigung, Technikeinsatz und Fairness
    in einem medialisierten Sport auseinander.</p>
<p><b>Christiane Zehrer</b> (Jahrgang 1980) stand jahrelang selbst als Fecht-Kampfrichterin an der Bahn und des Öfteren
    in der Kritik, wenn es um knifflige Entscheidungen über sekundenschnelle Aktionen ging. Aus dieser Erfahrung heraus
    ging sie theoretisch der Frage nach, weshalb Fecht- und andere Sportaktionen in so atemberaubendem Tempo vonstatten
    gehen - und Kampfrichter und Zuschauer bei deren Beurteilung so oft unterschiedlicher Meinung sind. Für ihre Arbeit
    zum Thema wurde sie 2003 mit dem Deutschen Studienpreis ausgezeichnet und publizierte u.a. "Der Mann in Schwarz und
    die Mattscheibe" in "Herr der Regeln. Der Fußball-Schiedsrichter (2006)". Sie hat in Hildesheim und Clermont-Ferrand
    (Frankreich) Angewandte Sprach- und Informationswissenschaft studiert, arbeitete u.a. für einen europäischen
    Sportverband in Paris und im Hochschul-Qualitätsmanagement. Daneben ist sie Redakteurin bei <a
            href="http://www.sciencegarden.de">sciencegarden.de</a>, einem Internetmagazin für junge Wissenschaft.</p>
