    Auswirkungen und Nebeneffekte haben dabei unterschiedliche Kommunikationswege und -mittel im Wandel der Zeit? Von
    Steintafel und Höhlenmalerei bis hin zu SMS und Blackberry? Wie schnell und effektiv ist Kommunikation tatsächlich
