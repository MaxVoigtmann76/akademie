<h2><a id="christ"></a>The fastest computer - ever (English talk) <img src="/img/en.png" width="15" height="10"
                                                                       style="border:1px solid white"
                                                                       alt="Englischer Programmpunkt"/></h2>
<p>We all are used to the rapid progress in computer technology: The time needed per elementary operation gets shorter
    and shorter rapidly. Where will this development lead? Landauer stated that 'information is physical'. Applying
    basic physical principles, ultimate limits on computation will be presented. For example, no matter how ingenious
    future physicists and engineers will be, no laptop (mass: 1 kilo, volume: 1 liter) is ever going to be faster than
    the 'ultimate laptop', which achieves roughly 10^51 operations per second on 10^31 bits. Examples of existing
    computers (partially) reaching some of the physical limits of computation will be presented.</p>
<p><b>Dr. Henning Christ</b> hat Physik in Marburg und Heidelberg studiert. Seine M.Sc. Arbeit fertigte er in Tucson,
    Arizona an. In den letzten Jahren hat er sich im Rahmen seiner Promotion in theoretischer Physik mit
    Quantencomputern in Festkörpersystem beschäftigt.</p>
