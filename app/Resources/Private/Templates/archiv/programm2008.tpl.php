<h2><a id="wladarsch"></a>Erlebte Zeit - Erzählte Zeit (Workshop)</h2>
<p>Zeit erscheint das Konstanteste zu sein, was wir im Leben kennen. Hier in unseren Sphären ist von relativistischen
