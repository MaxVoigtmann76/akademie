<h2><a id="baudson"></a>Time and Intelligence (English talk) <img src="/img/en.png" width="15" height="10"
                                                                  style="border:1px solid white"
                                                                  alt="Englischer Programmpunkt"/></h2>
<p>The relationship between time and intelligence consists in more than in the ability to read the clock properly. This
    is obvious even from a brief screening of the relevant literature: Are intelligent people different, or are they
    just faster -- for instance, with respect to such diverse aspects as reaction time, nerve conduction velocity,
    learning and discrimination time? Or are their inner clocks simply more accurate -- as revealed in more precise time
    estimations? These findings can also be applied to intelligence diagnostics, where time plays a role in respect to
    speeded testing or stability of intelligence over time -- the latter on an individual as well as on a population
    basis (Flynn effect). Development of intelligence over time is another of the numerous aspects of this exciting
    relationship, the variety of which I hope to convey in my presentation.</p>
<p><b>Dipl.-Psych. Tanja Gabriele Baudson, M.A.</b>, studied French and American literature and psychology in Bonn,
    Paris/France and Gold Coast/Australia. After stopovers in Giessen, Germany, and Nishinomiya, Japan, she has been
    working at the Department of Giftedness Research at Trier University since September 2007. Besides teaching and
    writing her Ph.D. thesis on diagnostic abilities of primary school teachers, she also works at the university's
    Center of Psychological Diagnostics testing potentially gifted children. In her scarce leisure time, she organizes
    the MinD Academy program with Martin Dresler and Anna Seemüller.</p>
