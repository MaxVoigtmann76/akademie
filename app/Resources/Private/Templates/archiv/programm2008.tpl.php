<h2><a id="wessel"></a>Kommunikation im Zeichen der Zeit (Vortrag)</h2>
<p>Nach zwei bis sieben Sekunden entscheiden wir bei einer ersten Begegnung mit jemandem über Sympathie oder Antipathie.
    Welche Zeichen sind es, die uns sekundenschnell zu einer bestimmten Blickrichtung gelangen lassen? Wieviel Zeit
