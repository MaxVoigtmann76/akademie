<h2><a id="cederbaum"></a>Was ist Gleichzeitigkeit? (Vortrag)</h2>
<p>Wenn ich am Berliner Dom stehend mit Dir telefoniere, der Du am Marburger Schloss spazieren gehst, können wir getrost
