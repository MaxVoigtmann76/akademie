<h2><a id="wolf"></a>Zeit in Sprache und Literatur (Vortrag)</h2>
<p>Zeit ist eine grundlegende Kategorie auch jedes Sprechens. Jede Sprache stellt unterschiedliche Mittel zur Verfügung,
    die Zeit als Zeitordnung, Zeitmodus und Zeitdauer auszudrücken. Literarische Gattungen sind ebenfalls so gestaltet,
    dass sie sich auf unterschiedliche Weise zur Zeit und zur Zeitlichkeit des dargestellten Sachverhalts beziehen.
    Erzählungen etwa beziehen sich immer auf vergangene Ereignisse, selbst Zukunftsromane sind in einer
    Vergangenheitsform geschrieben. Anhand mehrere Texte bzw. Textpassagen soll gezeigt werden, wie wichtig Zeit für das
    Sprechen (und Denken) der Menschen ist und wie die Zeit sprachlich gestaltet werden kann.</p>
<p><b>Prof. Dr. Dr. h.c. mult. Norbert Richard Wolf</b> ist emeritierter Professor der deutschen Sprachwissenschaft an
    der Universität Würzburg. Zu seinen Arbeitsgebieten gehört vor allem die semantisch fundierte Grammatik. Mehrere
    seiner Veröffentlichungen haben sich mit der Zeit und der Zeitgestaltung in der deutschen Sprache und in Texten
    befasst. Dazu kommt die Beschäftigung mit der Geschichte der deutschen Sprache; worüber ein Buch im Druck ist und im
    Herbst 2008 erscheinen wird.</p>
