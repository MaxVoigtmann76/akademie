<h2><a id="burdekat"></a>Zeit für Gesellschaft - Soziologische Perspektiven auf die Zeit (Vortrag)</h2>
<p>Der Vortrag will einigen Gedanken zum Zeitbegriff und ihrem Bezug zur Gesellschaft nachgehen. Ein historischer
    Rückblick steht am Anfang: Die "genaue" Zeiterfassung - sofern wirklich möglich - ist menschheitsgeschichtlich noch
    vergleichsweise neu. Sie zeichnet sich insbesondere dadurch aus, dass Zeit für jeden und überall mess- und ablesbar
