<h2><a id="brukamp"></a>Mind: A-Head of Time - How the brain structures time (English talk) <img src="/img/en.png"
                                                                                                 width="15" height="10"
                                                                                                 style="border:1px solid white"
                                                                                                 alt="Englischer Programmpunkt"/>
</h2>
<p>How does the brain perceive time? Does it construct its own kind of time? Neurobiological insights are only beginning
    to elucidate the mutual relevance of time and the brain on multiple different levels: (1) Chronobiology - on the
    time scale of hours, days, and years: Oscillators, both inside and outside of the brain, contribute to the creation
    of the circadian rhythm, and the molecular mechanisms of the clocks in these central and peripheral cells are
    becoming increasingly known. (2) Time perception - on the time scale of seconds and minutes: A variety of theories
    exists about the correlate or location for perceived time in the brain, and studies have described specialized areas
    in numerous regions as candidates for the mediation of temporal planning and subjective time awareness. (3) Sensory
    processing - on the time scale of milliseconds: Precision in sense percepts requires the exact temporal coding of
    stimuli in neurons, a responsibility that is shared by several mechanisms in individual cells and neural circuits.
    Many issues in the neurobiology of time surely remain unresolved until time will tell.</p>
<p><b>Dr. Kirsten Brukamp</b> studied medicine, philosophy, and cognitive science in Muenster and Osnabrueck and carried
    out research in cell, molecular, and developmental biology in Philadelphia and Boston.</p>
