    Kommunikationspartnern? Während der Präsentation werden wir Beispiele betrachten, die aufzeigen, dass moderne Medien
    eine faszinierende Addition wie auch eine komplexe Herausforderung der Kommunikation darstellen können. Wir werden
    diskutieren, welche Wege des Filterns von Informationsflut geeignet sind und welche - teilweise belustigenden -
    Tücken sich hinter moderner Kommunikation kurz- und langfristig verbergen. Dies wird eine interessante Anregung
    geben über Zeit und Gewicht persönlicher Begegnungen im aktuellen und individuellen Alltag nachzudenken.
