<h2><a id="zimmerli"></a>Philosophie der Zeit (Vortrag)</h2>
<p>Zu den Standardthemen der philosophischen und wissenschaftlichen Welterfassung gehört seit Beginn unserer
    abendländischen Tradition die Frage nach der Zeit. Von allem Anfang an sind die Beantwortungsversuche gekennzeichnet
    durch die logische Struktur der Paradoxie. In der Moderne finden sich neben der naturwissenschaftlichen eine
    phänomenologische und eine sprachphilosophische Genealogie des philosophischen Zeitdenkens. In der Gegenwart
    verschmelzen diese drei Genealogien zum Versuch eines integralen Zeitkonzepts. Vor diesem Hintergrund diskutiert der
    Vortrag die Frage, ob Zeit passiv erlebt oder aktiv konstruiert wird, und argumentiert, dass objektive Zeit
    subjektiv "gezeitigt" und daher i.e.S. "irreal", aber intersubjektiv wirksam ist.</p>
