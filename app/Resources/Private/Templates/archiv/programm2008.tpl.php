    unmittelbar nachvollziehbar ist. Diese Momente finden sich in nahezu jeder Kultur: Situationen, in denen Menschen
    sich gemeinsam in Ekstase bringen. Die wahrgenommene Zeit kann sich von der "tatsächlichen" erheblich unterscheiden
    - Gemeinsame Stunden mit Freunden scheinen binnen Minuten zu vergehen, wohingegen einige Minuten im Stau sich endlos
    in die Länge ziehen.</p>
