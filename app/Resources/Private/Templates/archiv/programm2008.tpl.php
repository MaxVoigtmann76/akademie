    unzeitgemäß. Dem lebenslangen Lernen wird querliegend das lebenslange Sterben als ethische Dimension der
    Zeitstruktur von Bildung gegenübergestellt.</p>
<p><b>Prof. Dr. Andreas Doerpinghaus</b>: Studium der Fächer Pädagogik, Germanistik, Philosophie und Geschichte an den
