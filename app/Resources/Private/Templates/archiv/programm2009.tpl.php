    evolutionsbiologische Belege und Theorien werden dargestellt und
    systematisiert. Schließlich wird auch die Frage näher beleuchtet, wie sich
    ein naturwissenschaftlicher Zugang überhaupt zu Phänomenen wie Glaube,
    Religiosität oder Mystik verhält.
</p>
