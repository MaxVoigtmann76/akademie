<h2 id="Prof.Dr.StefanAppelius">Flucht aus der DDR (Vortrag)</h2>
<p>Während der Periode zwischen 1961 und 1989, in der Deutschland von der Mauer geteilt wurde, versuchten viele tausend
    DDR-Bürger auf unterschiedlichen Wegen in den Westen zu gelangen. Solche Vorhaben waren strafbar, schon die bloße
    Absicht konnte eine Gefängnisstrafe nach sich ziehen. Aus welchen Motiven entschieden sich die Menschen für die
    Flucht in den Westen, warum flohen vor allem junge Menschen, welche Wege benutzten sie und wie kamen diejenigen,
    denen die Flucht gelang, mit dem Leben in der Bundesrepublik zurecht? Das sind einige der Fragen, die in dem
    geplanten Beitrag angeschnitten werden sollen. Ein besonderes Augenmerk soll auf die Fluchten über die "verlängerte
