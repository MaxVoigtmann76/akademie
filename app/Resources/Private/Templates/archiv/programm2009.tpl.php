<h2 id="Willenberg">Soziale Normen und Tabus - Grenzen des Alltags (Workshop)</h2>
<p>Haben Sie schon einmal im Restaurant den Gast am Nachbartisch gefragt, ob Sie von seinem Gericht kosten dürfen? Oder
    beim Einkaufen einen zufälligen Passanten gefragt, ob Ihnen das Outfit, das Sie gerade anprobieren, steht? Bei aller
    Individualität - Normen, Konventionen und Gewohnheiten bestimmen unseren Alltag und setzen enge Spielregeln sozialer
    Interaktion ohne dass wir uns ihrer bewusst sind. Diese Grenzen mögen bequem sein, sie beschränken aber unser
    Repertoire an Verhaltensmöglichkeiten und stehen uns oft auch einfach im Weg. In diesem Seminar setzen wir uns
    gezielt mit den sozialen Grenzen des Alltags auseinander, beschäftigen uns damit welche Grenzen ganz speziell für
    unsere Gesellschaft gelten und erproben an kleinen Beispielen, wie sie sich durchbrechen lassen. Die Teilnahme an
    den Beispielübungen wird ermutigt, ist aber natürlich nicht verpflichtend.</p>
<p><strong>Max Willenberg</strong>, Jahrgang 1984, seit 2005 Student der Ruhr-Universität Bochum, erwarb 2008 seinen
    B.Sc. in Wirtschaftspsychologie mit dem Thema der Kongruenz von Selbst- und Fremdwahrnehmung in
    Persönlichkeitsfragebögen. Gegenwärtig ist er Master-Student mit Interessensschwerpunkten in Eignungsdiagnostik und
    Training und arbeitet nebenberuflich als Tutor für die Grundlagenseminare der Fakultät.</p>
