<h2 id="Fackler">Die Finanzkrise - wie zu große unternehmerische Freiheiten (und unzureichende Statistikkenntnisse) die
    Weltwirtschaft an den Abgrund brachten (Vortrag)</h2>
<p>Die Finanzkrise von 2008 hat neben den wirtschaftlichen Folgen viel Wut erzeugt - es scheint, dass die Akteure in der
    Finanzbranche alle unfähig oder gierige Betrüger sind, oder beides. Mag dies in Einzelfällen auch zutreffen, so gibt
    es doch tiefere Gründe für die Krise:
    Viele Errungenschaften unserer Zivilisation haben das Leben fundamental verbessert und sind unverzichtbar geworden,
    können aber gleichzeitig großen Schaden anrichten. Man denke z.B. an Feuer, Messer, Sprengstoff oder Medikamente. Es
    ist sehr schwierig, die Nutzung dieser Dinge weitgehend zu erlauben und gleichzeitig ihren Missbrauch weitgehend zu
    verhindern. Die Entscheider über FREIHEIT und GRENZEN sind hier in einem Dilemma und müssen die Regeln/Gesetze oft
    permanent nachjustieren. Und so gibt es auch in der modernen Geldwirtschaft viele Elemente, die gleichzeitig Segen
    und Fluch sein können und demzufolge diesem Wechselspiel aus Liberalisierung und Regulierung unterliegen. Der
    Vortrag wird einige von ihnen erläutern und klar machen, dass die heutigen Probleme primär struktureller Art und
    nicht die individuelle Schuld Einzelner sind. Ausführlich eingegangen wird zudem darauf, wie Risiken in der
    Finanzwirtschaft strukturiert werden. Dies wird in den meisten Darstellungen zur Finanzkrise vornehm übergangen -
    wohl weil es mit Mathematik zu tun hat. Der Vortrag wird diese Lücke schließen - und dabei allgemeinverständlich
    bleiben. Man braucht nämlich keinerlei Statistikkenntnisse, sondern nur ein gewisses Interesse an Zahlen, um
    intuitiv zu verstehen, wie die Diversifikation von Risiken funktioniert, und wie nützlich / gefährlich / schwierig /
    frustrierend / spannend sie ist.
</p>
<p><strong>Michael Fackler</strong> stammt aus einer Allgäuer Musikerfamilie und hat in München und Pisa Mathematik
    studiert. Er ist freiberuflicher Aktuar (geprüfter Versicherungsmathematiker). In langjähriger Tätigkeit bei
    Rückversicherungen musste er regelmäßig Risiken kalkulieren, die so strukturiert sind wie die in der Finanzkrise
