<h2 id="VanitasBerrymore">Das Leib-Seele-Trilemma - Freiheit vs. Determinismus - Lösungsansätze</h2>
<p>Die Frage, ob es einen freien Willen gibt, in einer Welt, die als kausal determiniert gedacht wird, ist eine Frage,
    die (wenn auch in anderer Formulierung) zu den philosophischen Klassikern gehört.
    Dieser Vortrag soll hierbei zunächst eine Klärung der Begriffe Freiheit, Kausalität etc. bringen und eine
    Konkretisierung des Problems (bzw. eine Lokalisierung des Widerspruchs) darstellen, um anschließend eine Übersicht
    über verschiedene Ansätze zu liefern, wie dieser Widerspruch aufgelöst werden kann.
    Vermutlich wird jeder, der sich über dieses Thema seine Gedanken gemacht hat, sich selbst in der ein oder anderen
    Position wiederfinden und seine eigene Position rational be- und durchleuchtet sehen.
