<h2 id="Wladarsch">"Die Gedanken sind frei" - künstlerische Freiheit in der Literatur (Vortrag)</h2>
<p>Ausgehend von dem Volksliedtext, der auf Wurzeln aus der Antike zurück blicken kann, sollen die verschiedenen Aspekte
    von Freiheit in der Arbeit von Schriftstellern untersucht werden. Dabei dienen uns auch Beispiele aus der
    (Welt-)Literatur als Leitfaden. Zu nächst ist da der Gedanke - wie frei ist er eigentlich? Darüber sollen uns
    Wittgenstein, Lem und die Konkrete Poesie Auskunft geben. Welche Erkenntnisse kann die Gehirnforschung dazu
    beisteuern? Wie ist das Verhältnis von Wissenschaft und Kunst? Die Arbeit der Schreibenden, vom Gedanken zum Werk.
    "An die Nachgeborenen" oder wer liest eigentlich was wir schreiben? Die Rolle der Medien. Selbstzensur und die
    Zwänge des Schreibens. Selbstzeugnisse von Schriftstellern z.B. von Stephen King. Wie die Lebensumstände die
    Gedankenfreiheit einschränken. Der Balanceakt zwischen Brotberuf, Leben und Schreiben. Die Gefahren des
    Veröffentlichens: Ruhm tötet und "Writers in Prison". Gedankenkontrolle in Orwells "1984". Wo bleibt das Positive?
    Schreiben kann für jedermann befreiend sein: Das "Freedom Writers" Projekt beschreibt wie "schwierige Jugendliche"
    über das Schreiben neue Perspektiven für ihr Leben gewinnen konnten.</p>
<p><strong>Stefan Wladarsch</strong>, 1966 in Ingolstadt geboren, in der Kindheit als Legastheniker eingestuft und dabei
    HB getestet; Studium der physikalischen Technik an der FH München; Mitarbeit in der Chaosgruppe der TU-München (u.a.
    AK Philosophie; Mitarbeit bei einem Seminar über Nietzsche und Chaos); Seit der frühsten Jugend Kontakt zur
    Theaterarbeit (seit 2002 Jahren vor allem als Impro-Theaterspieler) und seit mehr als 25 Jahren schriftstellerisch
    tätig (u.a. mehrere veröffentlichte Kurzgeschichten, regelmäßige Lesungen von Poesie); Arbeitet im Brotberuf als
    Qualitätsmanager und "Teilzeitvater".</p>
