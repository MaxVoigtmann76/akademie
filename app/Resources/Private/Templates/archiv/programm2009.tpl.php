    wollen? Ist es vorstellbar, dass der weitgehende Verzicht auf Datenschutz
    in einer modernen Informationsgesellschaft erst die Freiheit ermöglicht,
    die von der Netzcommunity angestrebt wird? Der Vortrag geht auf diese
    Fragestellungen ein und setzt sich kritisch mit den technischen und
    politischen Kompromissen, die einen Ausgleich verschiedener Interessen
    anstreben, auseinander.
</p>
<p><strong>Prof. Dr.-Ing. Ulrich Greveler</strong>Jahrgang 1972, ist Professor für Informatik
    am Fachbereich Elektrotechnik und Informatik der Fachhochschule Münster und
    leitet dort das Labor für IT-Sicherheit. Er publiziert zu den Themen
