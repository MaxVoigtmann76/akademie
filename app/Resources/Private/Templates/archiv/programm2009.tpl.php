<h2 id="Prof.Dr.ThomasBuchheim">Was Freiheit braucht und was nicht (Vortrag)</h2>
<p>Freiheit, im Sinne von echter Willensfreiheit, braucht nicht nur spontanes Handlungsvermögen der Person und die
    Veränderlichkeit unserer physischen Körperzustände in Abhängigkeit von Qualifikationen, die nur unsere Gedanken,
    nicht aber körperliche Vorgänge (wie z.B. Neuroprozessen) haben können, sondern sie braucht drittens auch die
    Wahrheit der These, dass wir im Augenblick des Wollens und Handelns /anders gekonnt hätten/, als wir tatsächlich
    tun. Der Vortrag macht in Beziehung auf dieses dritte plausibel, dass solche alternativen Möglichkeiten auch dann
    bejaht werden können, wenn der freie Wille der Person nicht, wie z.B. Kant meinte, aus den kausalen Zusammenhängen
    der des übrigen Weltgeschehens ausgenommen wird. - Freier Wille braucht insofern /nicht /die empirische
    Unbedingtheit unseres Willensvermögens.</p>
<p><strong>Prof. Dr. phil. Thomas Buchheim</strong>, geb. 1957, Professor der Philosophie an der
    Ludwig-Maximilians-Universität München. Geschäftsführender Herausgeber des Philosophischen Jahrbuchs der
    Görresgesellschaft. Mitglied der Schelling-Kommission und Mitherausgeber der historisch-kritischen Schellingausgabe
    der Bayerischen Akademie der Wissenschaften. Werdegang: Studium der Philosophie, Gräzistik und Soziologie in
    München, 1993-99 Professor für Philosophie in Mainz; seit 2000 Ordinarius an der LMU in München.
    Wichtigste Buchveröffentlichungen: Die Sophistik als Avantgarde normalen Lebens, Hamburg (1986); Eins von Allem. Die
    Selbstbescheidung des Idealismus in Schellings Spätphilosophie, Hamburg (1992); Die Vorsokratiker. Ein
    philosophisches Porträt, München (1994); Aristoteles, Freiburg (1999); Unser Verlangen nach Freiheit, Hamburg
    (2006).
</p>
