<h2 id="DavidEwert">Extreme Armut: Die Grenzen der Freiheit in unserer Welt (Vortrag)</h2>
<p>In der unseren Welt erzwingt extreme Armut die bei weitem größte Beschränkung individueller Freiheit, da sie vielen
