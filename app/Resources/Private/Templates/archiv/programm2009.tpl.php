    Universität Hamburg abgeschlossen und arbeitet seitdem als selbstständige Dolmetscherin. Parallel arbeitet sie an
    ihrer Promotion. Seit Beginn ihres Studiums bewegt sie sich beruflich und privat sowohl in der Welt hörender als
    auch der Welt gehörloser Menschen.</p>
