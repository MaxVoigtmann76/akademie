<h2 id="MareikeWinkler">Deutsche Gebärdensprache (DGS) (Workshop)</h2><p>In Deutschland leben ca. 80.000 gehörlose
    Menschen, die primär in Deutscher Gebärdensprache (DGS) kommunizieren. Die DGS ist eine eigenständige, vollwertige
    Sprache, die im Gegensatz zur Lautsprache (auditiv-verbal) völlig andere Modalitäten verwendet, da sie eine
    visuell-gestische Sprache ist. In diesem Schnupperkurs könnt Ihr Euch ein wenig an diese ganz andere Sprache
    herantasten und ausprobieren. Dazu gehören natürlich das Fingeralphabet, einzelne Gebärden und auch schon einige
    Sätze. Darüber hinaus werden auch erste Einblicke in die Kultur gehörloser Menschen gegeben. Ihr braucht für diesen
    Workshop insbesondere Eure Arme, Euren Oberkörper, das Gesicht (für die Mimik) und Eure Augen. Was Ihr nicht
    braucht, sind Eure Stimme und Eure Ohren, denn die benötigen wir zur Kommunikation in DGS nicht.</p>
