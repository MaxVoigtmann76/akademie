<h2 id="Dübel">Antik&ouml;rper: Neue Tricks mit altbekannten Molek&uuml;len. (Vortrag)</h2>
<p>Unser K&ouml;rper kann sich gegen viele infekti&ouml;se Eindringlinge sehr erfolgreich mit hochspezialisierten
    Abwehrstoffen, den sog. Antik&ouml;rpern, wehren. Von diesen Molek&uuml;len bildet jeder Mensch Hunderte von
    Millionen unterschiedliche. Biotechnologisch k&ouml;nnen menschliche Antik&ouml;rper heute au&szlig;erhalb des K&ouml;rpers
    in gro&szlig;en Mengen hergestellt werden. In den letzten 10 Jahren ist die Zahl der therapeutischen Anwendungen von
    Antik&ouml;rpern stark gestiegen, und die Zahl neuer therapeutischer Strategien mit Antik&ouml;rpern w&auml;chst st&auml;ndig
    weiter. Aber auch in der Forschung und Diagnostik besteht ein stark steigender Bedarf. </p><p>

    Eine Schl&uuml;sseltechnologie f&uuml;r die breitere Verwendung insbesondere menschlicher Antik&ouml;rper war die
    Erfindung des &quot;Phagen-Displays&quot;. Damit k&ouml;nnen wir einen einzelnen Antik&ouml;rper (gegen eine
    bestimmte Substanz) aus dem enorm diversen Repertoire verschiedenster Antik&ouml;rper in unserem K&ouml;rper &quot;herausfischen&quot;.
    Das ganze funktioniert komplett im Reagensglas (&quot;in vitro&quot;), so dass auch die bis dahin notwendigen
    Tierversuche komplett vermieden werden. </p><p>

    Das &quot;Phagen-Display&quot; erm&ouml;glicht es im Besonderen, gleich jenes Erbsubstanz-St&uuml;ck (Gen) zu
    gewinnen, welches den Bauplan f&uuml;r den Antik&ouml;rper enth&auml;lt. Dieses Gen kann vielf&auml;ltig bearbeitet
    werden, um die Eigenschaften des Antik&ouml;rpers zu verbessern oder sogar v&ouml;llig neue Funktionen anzuf&uuml;gen.
    Wir haben z.B. einen katalytischen Eiwei&szlig;stoff (ein &quot;Enzym&quot;) gentechnologisch an Antik&ouml;rper
    angeh&auml;ngt, um so Krebszellen effektiver bek&auml;mpfen zu k&ouml;nnen.</p><p>

    Eine v&ouml;llig andere, aber nicht weniger wichtige Anwendung des &quot;Phagen-Displays&quot; ist die Herstellung
    von Forschungssonden. Zwar kennen wir die komplette menschliche Gensequenz, aber es fehlt f&uuml;r weite Teile noch
    am Verst&auml;ndnis, was ihre einzelnen Teile bedeuten. Abhilfe b&ouml;ten Antik&ouml;rper gegen alle etwa 25.000
    verschiedenen Eiwei&szlig;e - und mit dem &quot;Phagen-Display&quot; k&ouml;nnten wir diese schon heute erzeugen.

</p><p><strong>Prof. Dr. rer. nat. Stefan D&uuml;bel, Diplom-Biologe</strong> leitet die Abt. Biotechnologie an der TU
    Braunschweig. Der Gegenstand seiner Forschung ist Erschlie&szlig;ung neuer Wege zur Herstellung und
    Charakterisierung menschlicher Proteine zur therapeutischen Anwendung. Die Entwicklung des Antik&ouml;rper-Phagendisplays
    (zusammen mit Frank Breitling) erm&ouml;glichte die tier-unabh&auml;ngige Erzeugung von Antik&ouml;rpern und damit
    neue Wege zu vertr&auml;glicheren Antik&ouml;rper-Therapeutika. Dar&uuml;ber hinaus wurden neuartige Antik&ouml;rper-Fusionsproteine
    entwicklet, gem&auml;&szlig; des Leitsatzes, dass Proteinwirkstoffe m&ouml;glichst vollst&auml;ndig aus Anteilen
    humanen Ursprungs bestehen sollten. So wurde z.B. ein neuartiges &quot;Immuntoxin&quot; mit einem komplett
    menschlichen cytotoxischen Anteil konstruiert. Seit 2005 wurden diese in-vitro Antik&ouml;rper-Herstellungsmethoden
    auch immer st&auml;rker f&uuml;r die Grundlagenforschung genutzt, und es ist nun m&ouml;glich, Antik&ouml;rper gegen
    jedes menschliche Protein zu entwickeln, welche &uuml;berdies in ihrem Funktionsspektrum den herk&ouml;mmlichen
    Antik&ouml;rpern aus Tieren &uuml;berlegen sind.
    Stefan D&uuml;bel hat >170 wissenschaftliche Publikationen verfasst, 4 B&uuml;cher zum Thema herausgegeben und ist
    Miterfinder auf mehr als 20 Patenten.
</p><p><a href="#uebersicht" class="zur">Zur&uuml;ck zur Programm&uuml;bersicht</a></p>


