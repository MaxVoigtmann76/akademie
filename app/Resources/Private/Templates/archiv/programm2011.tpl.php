<h2 id="Kalwies">Libertarismus/Anarchokapitalismus - Die Zukunft der Gesellschaft? (Vortrag)</h2>
<p>
    Anarchokapitalismus? Aber ist Anarchie nicht das Gegenteil von Kapitalismus?</p><p>
    Mag sich mancher fragen. Die Wikipedia sagt dazu: "Der Anarchokapitalismus (auch Marktanarchismus oder
    Anarcholiberalismus) ist eine politische Philosophie, die f&uuml;r eine vom freien Markt, freiwilligen &Uuml;bereink&uuml;nften
    und freiwilligen vertraglichen Bindungen gepr&auml;gte Gesellschaft ohne staatliche Institutionen und Eingriffe
    eintritt. Er tritt f&uuml;r ein Recht auf Selbstbestimmung und eine weitreichende Verf&uuml;gungsgewalt &uuml;ber
    Privateigentum ein. Anarchokapitalisten betrachten den Staat als illegitimes politisches System, das
    Gesellschaftsmitglieder in ihrer Freiheit beschr&auml;nkt, unrechtm&auml;&szlig;ig Gewalt gegen diese aus&uuml;bt
    und sie durch Steuererhebung beraubt."
