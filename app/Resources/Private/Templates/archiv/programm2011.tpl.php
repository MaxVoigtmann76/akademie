    wurde. Der Beitrag gibt Einblicke in die Schwierigkeit der Bew&auml;ltigung der Komplexit&auml;t und der
    Unsicherheit der Unternehmensplanung. Ferner werden Ziele und Fallstricke bei der Unternehmensplanung
    angesprochen.</p>
