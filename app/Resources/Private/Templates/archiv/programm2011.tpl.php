    und Hintergr&uuml;nde f&uuml;r Konflikte aufdecken k&ouml;nnen, auf die man intuitiv schwerlich kommen w&uuml;rde.
    Der Vortrag stellt wichtige Kulturdimensionen vor und zeigt an Beispielen, was diese f&uuml;r Staaten, Firmen und
    Individuen bedeuten. Dabei werden u.a. folgende Fragen gestreift:
</p>
<p>
<ul>
    <li>Ist der Imperialismus des Westens vorbei?</li>
