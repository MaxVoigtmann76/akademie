    klassischen Liberalen wie Locke, Hume, Smith, und Thomas Jefferson, den klassischen Anarchisten, wie Henry David
    Thoreau als auch auf den moderneren &Ouml;konomen wie Ludwig von Mises, Friedrich Hayek, Murray Rothbard, David
    Friedman oder Hans Herrmann Hoppe. </p><p>
    In den Vereinigten Staaten eine kraftvolle und weithin bekannte Gruppe (durch Libert&auml;re wie Clint Eastwood,
