<h2 id="Zagermann">Was ist Stringtheorie? (Vortrag) </h2>
<p>
    Eine der gr&ouml;&szlig;ten Leistungen der Physik des 20. Jahrhunderts war die Entwicklung zweier Theorien, die die
    uns bekannten physikalischen Ph&auml;nomene auf vier grundlegende Wechselwirkungen zwischen einem Dutzend
    verschiedener Typen von "Elementarteilchen" zur&uuml;ckf&uuml;hren. Das sogenannte Standardmodell der
    Elementarteilchenphysik beschreibt dabei die elektromagnetische, die schwache und die starke Wechselwirkung zwischen
    subatomaren Teilchen, w&auml;hrend die Allgemeine Relativit&auml;tstheorie die derzeit beste Theorie der Gravitation
    ist. Die Stringtheorie ist der ehrgeizige Versuch, diese Vielfalt an Elementarteilchen und Wechselwirkungen auf nur
    eine fundamentale Wechselwirkung zwischen nur einer Art von Objekten - den "Strings" - zur&uuml;ckzuf&uuml;hren. Was
    genau damit gemeint ist, soll in diesem Vortrag einf&uuml;hrend erl&auml;utert werden.
</p>
<p>
    <strong>Jun.-Prof. Dr. Marco Zagermann</strong> studierte an der Universit&auml;t Hamburg Physik und promovierte an
    der Pennsylvania State University in theoretischer Elementarteilchenphysik. Nach Postdocaufenthalten in Halle, am
    CERN in Genf und der Stanford Universit&auml;t leitete er eine Emmy-Noether-Nachwuchsgruppe am Max-Planck-Institut f&uuml;r
    Physik in M&uuml;nchen. Seit 2009 ist er Juniorprofessor an der Leibniz Universit&auml;t Hannover
    und Leiter der Forschungsgruppe "Stringph&auml;nomenologie und -kosmologie" am Exzellenz-Cluster QUEST.
    In seiner Forschung besch&auml;ftigt er sich mit den ph&auml;nomenologischen Konsequenzen der Stringtheorie,
    insbesondere im Hinblick auf Kosmologie und Astroteilchenphysik, sowie mit Gravitationsaspekten der Stringtheorie
    und der Supersymmetrie.</p>
