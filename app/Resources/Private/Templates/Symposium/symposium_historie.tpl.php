<h1>Geschichte des Symposiums und vergangene MHN-Symposien</h1>

<p>Nach sieben MinD-Akademien entstand 2009 die Idee, das erfolgreiche Konzept eines interdisziplinären Kongresses mit jährlich wechselndem Leitthema auch für Teilnehmer über eine studentische Zielgruppe hinaus anzubieten. Während des Mensa-Jahrestreffens 2009 bis 2013 konnten Mitglieder von MHN und Mensa oder andere Interessierte im Rahmen des eintägigen Symposiums interessanten Vorträgen lauschen und anschließend lebhaft diskutieren. Seitdem befand sich das Symposium in einem Dornröschenschlaf. Ab 2019 soll es eine Neuauflage des Symposiums unter dem Namen MHN-Symposium geben.</p>

<p>Ein Symposium ist ursprünglich, z. B. bei Xenophon und Platon, ein geselliges, lehrreiches Beisammensein, meist beim Essen. Beim MHN-Symposium wird zwar nicht gegessen, aber Vorträge von wissenschaftlichen Experten*innen als Diskussionsgrundlage für ein jährlich wechselndes Oberthema genutzt. Die genaue Konzeption des Symposiums kann kreativ verändert werden. Zentral ist der im Gegensatz zur Akademie starke Bezug zu lokalen Hochschulen und ihrem Profil, sowie die Zusammenarbeit mit Mensa in Deutschland e. V.</p>


